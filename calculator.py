sayi1 = float(input("1. Sayı =>"))
islem = input("İşlemi Giriniz (+,-,*,/) => ")
sayi2 = float(input("2. Sayı =>"))

if islem == "+":
    sonuc = sayi1 + sayi2
    print(sonuc)
    
elif islem == "-":
    sonuc = sayi1 - sayi2
    print(sonuc)
    
elif islem == "*":
    sonuc = sayi1 * sayi2
    print(sonuc)
    
elif islem == "/":
    sonuc = sayi1 / sayi2
    print(sonuc)
    
else:
    print("Hatalı Giriş Yaptınız..!")





    

